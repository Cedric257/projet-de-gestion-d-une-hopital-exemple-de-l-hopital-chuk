<h2>Departement de Pediatrie</h2>
<p>Ce departement comprend en son sein un poste de Pediatrie.
Ce poste comporte differents services a savoir:
<ul>
	<li>Consultation interne</li>
	<li>Urgence Pediatrique</li>
	<li>Stabilisation Therapetique</li>
	<li>Neonalogie</li>
	<li>Hospitalisation</li>


</ul>
<ol><li> Consultation externe :</li>
	<ul>
 <li> Accueil des patients/</li>
 <li>Consultation (Examens, prescription des médicaments, conseils)</li>
 <li>Prise en charge de quelques urgences (les convulsions, fièvre, désobstruction nasale…)</li>
 <li>Analyse des résultats de laboratoire</li></ul>
<li>Urgence pédiatrique :</li><ul>
 <li>Les premiers soins avant hospitalisation</li>
 <li>Déshydratation</li>
 <li>Convulsion entérite</li>
 <li>Détresse respiratoire</li>
 <li>Les douleurs intenses</li>
 <li>Crise d’asthme</li>
 <li>Fièvre</li>
 <li>Anémies</li>
 <li>Consultation (la nuit et les week-end</li>)</ul>
<li>Stabilisation thérapeutique :</li>
<ul>
 <li>Prise en charge des cas de malnutrition (aigue sévère et aigue modérée)</li></ul>
<li>Néonatologie :</li><ul> 
 <li>Prise en charge des prématurés</li>
 <li>Asphyxie périnatale</li>
 <li>Macrosomie et hypoglycémie</li>
 <li>Détresse respiratoire néonatale</li>
 <li>Malformations congénitales à caractère urgent</li>
 <li>Prise en charge des sous-poids (-1kg750g)</li>
 <li>Des nouveaux nés Hypothermiques</li>
 <li>Omphalocèle</li></ul>
 <li>Hospitalisation :<ul> Prise en charge des patients hospitalises</li></ul>
</ol>
</p>