
<h2>DEPARTEMENT DES LABORATOIRES</h2>

Ce département est composé de 3 services à savoir :

<ol>
<li>biologie</li>

<li>biochimie</li>

<li>anatomopathologie</li>
</ol>
Les services offerts :
<ul>
<li>Hématologie :</li>

<p>Numération Formule Sanguine (NFS),Vitesse de sédimentation (VS),Myélogramme, Frottis sanguin, Bilan de coagulation (TP-TCK, INR),Test d’Emmel, Taux des réticulocytes

Examen cytobactériologique des urines (ECBU), Examen cytobactériologique des liquides de ponction (LCR, LA, LP), Examen bactériologique des pus divers, Hémoculture, Coproculture Spermogramme et Spermoculture.</p>

<li>Biochimie :</li>

<p>Glycémie, Urée, créatinine, GOT, GPT, gamma GT, PAL Bil T et C, progestérone, testostérone, prolactine, T3,T4, FSH, FT3, FT4), Hyperglycémie provoquée, Triglycéride, Cholestérol (Total, HDL et LDL), PSA, Dosage de l’Albumine, Electrophorèse de l’ Hb, Protéinurie de 24h, Amylasémie, Amylasurie, CPK, LDH, Lipasémie lonogramme (Ca2+, Na+, K+, Mg2+, Cl-…).</p>

<li>Sérologie :</li>

<p>TPHA ; VDRL ; CRP ; Séro-widal ; Antigène HBs ; Anticorps anti-HVC ; Toxoplasmose ; Test de grossesse ; Arthritest ; ASLO ; VIH. Anatomo-Pathologie : Cytologie : frottis cervico-vaginale ; cytologie non gynécologique ; spermogramme.</p>

.<li>Histologie :</li>

.<p>Biopsie ; pièce opératoire Mycobactériologie : Recherche de BAAR, Examen mycologiques (ongles, cheveux, squames), Examen direct des selles, Enrichissement des selles par la méthode de Ritchie, Gouttes épaisse<p>
<li>Transfusion :</li>

<p>Groupage sanguin et Rhésus, Tests de compatibilité

Heure de remise des résultats

Les examens d’urgence sont remis le même jour à partir de 15heures : la glycémie, GE, la créatinine, l’urée, le LCR, NFS, lonogramme, TP-TCK, BilT&C.

D’autres résultats peuvent être remis à partir du 3ème au 2ème jour (hormones, ECBU, prélèvements génitaux et mycologique, marqueurs tumoraux, tirage toxoplasmose et myélogramme).

Le reste des résultats sont remis le lendemain à partir de 15 heures.</p>
</ul>