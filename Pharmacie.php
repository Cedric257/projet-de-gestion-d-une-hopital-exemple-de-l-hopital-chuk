
<h2>DEPARTEMENT DES PHARMACIES</h2>

Ce département comprend :

<br>-La Pharmacie centrale</br>

<br>-La Pharmacie vente (Officine)</br>

1) Pharmacie centrale

<p>Elle sert des médicaments aux unités de gestion, comme unité de gestion Gynéco, unité de gestion Médecine Interne, unité de gestion Urgence et unité de gestion Bloc Opératoire et Réanimation.</p>

2) Pharmacie vente (Officine)

<p>Cette pharmacie dispose d’un stock principal et de trois cages de produits pharmaceutiques pour la conservation des médicaments.</p>

<p>Le département des Pharmacies s’occupe principalement de la gestion des médicaments en général (l’expression des besoins en intrants médicaux) ou de la gestion du cycle logistique de tous les produits de santé du CHUK, des stocks principaux et périphériques (unité de gestion) y compris les instruments d’orthopé</p>