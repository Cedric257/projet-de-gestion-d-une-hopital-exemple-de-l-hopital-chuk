<p>Le Centre Hospitalo-Universitaire de Kamenge (CHUK) lance un avis d’Appel d’Offres Ouvert National N° DNCMP / 209 /F/2022-2023 POUR LA FOURNITURE D’UNE CAMIONNETTE DOUBLE CABINE 4*4 TROPICALISE ET DEUX VOITURES DU CENTRE HOSPITALO-UNIVERSITAIRE DE KAMENGE.</p>

<p>Ce marché est financé sur Fonds propres du CHU Kamenge, exercice 2022-2023.
La participation à la concurrence est ouverte, à égalité de conditions à toute personne physique et morale et possédant les conditions techniques, juridiques et financières nécessaires à l’exécution du marché.</p>

<p>Le dossier d’appel d’offres peut être consulté et obtenu auprès du Secrétariat de la Direction Générale du CHU Kamenge.
L’achat du Dossier d’Appel d’Offres est obligatoire pour tout soumissionnaire faisant une offre.</p>

<p>Le paiement d’un montant non remboursable de 100. 000FBU (cent mille francs burundais) se fera comme suit : La moitié sera versée sur le compte n° CC10296 ouvert à la BRB au nom du CHUK et l’autre moitié sera versée au sous-compte de transit des recettes non fiscales N° CC 10003 ouvert à la BRB.</p>

