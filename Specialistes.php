<h2>DEPARTEMENT DES SPECIALITES</h2>

<p>Le département des spécialistes à trois services :
<br>-ORL</br>
-Ophtalmologie
<br>-Stomatologie</br> </p>

<h4>1. ORL (Oto-Rhino-Laryngologie)</h4>
<p>- Otologie s’occupe des maladies de l’oreille, corps étrangers, otite, dépôt du bouchon de cérumen, traumatisme de l’oreille, bourdonnement de l’oreille, hypo-acousie, surdité, fistule, otalgie, perforation tympanique, vertiges, mastoïdite, trouble de l’audition ...</p>
<br>- Rhinologie s’occupe des maladies des fosses nasales (Epistaxis, Fracture nasale, Polypose naso sinusienne, Rhinites aigues) et sinus (Ethmoïdite, Sinusite maxillaire; frontal sphénoïdale).</br>
<br>- Laryngologie s’occupe des pathologies du :</br> 
<br>-du larynx (Laryngites aigue et chronique, Képiallomatise laryngé, Tumeurs du larynx)</br>
-du pharynx (Syndrome d’apnée du sommeil, Rhinopharyngites de l’enfant, Tumeurs pharyngé).
<br>-de la Glande salivaire : -Tumeurs des glandes
salivaires</br>
<br>-Pathologie salivaire médicale</br>
<br>-du cou (Adénopathie cervicale, Fistule et kyste congénitaux du cou, Traumatisme cervicale, Lymphangiomes, Tumeurs thyroïdiens).
Les examens complémentaires en ORL sont :</br>
<br>-Audiométrie tonale pour détecter le degré de l’audition aérienne qu’osseuse</br>
<br>-Laryngoscopie</br>
<br>-Panendoscopie</br>
<br>-Echographie / Radiologie</br>
