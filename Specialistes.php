<h4>3. Stomatologie</h4>
<p>Le service de stomatologie se subdivise en deux parties ; Dentisterie
et Stomatologie :</p>
<br>- La Dentisterie s’occupe des pathologies de la dent seulement.</br>
<br>- La Stomatologie s’occupe des pathologies de la bouche et des dents</br>

<p>Exemple des pathologies traitables dans ce service sont :
Les pathologies dentaires, les pathologies des gencives, les pathologies muqueuses, la mauvaise haleine, noma (maladie misère), tumeur de la bouche, les fractures de la mâchoire. Petites interventions du service Stomatologie :</p>
<br>-détartrage</br>
-extraction
<br>>-obturations<br>
-exérèse (épulis ou kyste buccal)
<br>>-immobilisation (fracture mandibulaire ou maxillaire)<br>
-réduction
<br>-suture des plaies buccales</br>
-traitement esthétique si les dents qui sont en mauvaise position.