<h2>DEPARTEMENT GYNECO-OSTETRIQUE</h2>

Ce département comprend deux sous services :
<ol>
<li>Gynécologie</li>
<li>Obstétrique</li>
</ol>

Est logé dans trois bâtiments dont :
<ol>
<li> Bâtiment abritant les consultations externes qui reçoivent en
ambulatoire :</li>
<br>-Les patients présentant des pathologies survenant en dehors de la grossesse<br>
-Des femmes qui viennent pour un examen de dépistage (cancer du col- du sein- du Diabète gestationnel, etc.)
<br>-Le suivi et la prise en charge des grossesses<br>
-La prise en charge des complications mineurs du post partum et du post opératoires
<li> <p>Bâtiment pour la maternité et le bloc opératoire. C’est dans ce bâtiment où se déroule la gestion des accouchements simple ou compliqués, la prise en charge des grossesses pathologiques, les interventions obstétricales (Césariennes, hystérectomies
obstétricales et gynécologiques : voie haute ou voie basse,
les myomectomies et autres interventions gynécologiques et obstétricales), la sénologie</li></p>
<li><p> Bâtiment pour la prise en charge et suivi des patients hospitalisées pour des causes     gynécologiques ou obstétricales
Le Département de Gynécologie –Obstétricale s’occupe de l’enseignement des étudiants de deuxième cycle (Baccalauréat au Doctorat de Médecine Générale) et troisième cycle de Médecine (Tout le cycle complet de cinq ans).</li>
</ol></p>