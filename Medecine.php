<h2>DEPARTEMENT DE MEDECINE INTERNE</h2>

<br>Ce département est essentiellement composé de :<br>

<h4>1.Unité d’Hospitalisation qui comprend ;</h4>

<br>- service de pneumologie</br>

- service de médecine interne et infectiologie

<br>- service de neurologie et endocrinologie</br>

- service de cardiologie et néphrogie

<br>- service de plastro-anthologie</br>

<h4>2. Unité de consultation externe qui comprend :</h4>

<br>-CPAMP (Centre de Prise en charge Ambulatoire et Multidisciplinaire des PPVIH/SIDA)</br>

<=-Unité d’exploration : endoscopie, écographie cardiaque, électrocardiographie

<br>-Unité d’archivage La consultation externe s’occupe :</br>

-des soins spécialisés des patients référés par les autres structures sanitaires

<br>-des soins des patients déjà reçu au urgence médicales en cas de besoin</br>

<br>-des soins des patients ambulants pour suivi des maladies chroniques</br>
<ul>
<br>Ce département a une unité psychosociale qui s’occupe de :</br>
</ul>
<br>1. La consultation psychologique</br>

2. La thérapie individuelle et familiale

<br>3.L’éducation et observance thérapeutique en cas des maladies Chroniques</br>

4. L’écoute et conseil

<br>5. La formation continues pour le personnel soignant et non soignant</br>

6. L’Orientation et facilitation de la prise en charge médicale des patients démunis

<br>7. Counseling pour le dépistage volontaire</br>

8. La révélation de statut sérologique pour les enfants

<br>9. La référence et contre référence</br>

10. L’animation ou encadrement de groupes de paroles

<br>11. La participation aux réunions d’échange avec les associations</br>

<br>12. Le contact avec les organisations partenaires (PAM, FHI360, PSI, Orphelinats, …)</br>

13. La séance d’éducation pour la santé (EPS)

<br>14. L’encadrement des stagiaires Psychosociaux</br>